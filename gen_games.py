# dedup نهائي لكل بنوك القوائم (أمان)
def _dedup_pairs(lst):
    s=set(); o=[]
    for it in lst:
        key=it[0] if isinstance(it,list) else it
        if key not in s: s.add(key); o.append(it)
    return o
